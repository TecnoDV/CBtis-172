    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        double Estatura, Peso; double IMC;
        String Nombre;
        Peso = Float.parseFloat(jTextField1.getText());
        Estatura = Double.parseDouble(jTextField2.getText());
        Nombre = String.valueOf(jTextField4.getText());
        IMC = Peso / (Estatura * Estatura);
        JOptionPane.showMessageDialog(null, Nombre + "Tu IMC Es: " + IMC, "El IMC Tomando Encuenta Tu Peso y Estayura", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jButton2ActionPerformed

